# Introduction to Tuples

# Tuples are immutable. It cannot be modified after creation
tuple_fruit = ("Oranges", "Apples", "Bananas")
tuple_num = (12, 14)
tuple_add = tuple_fruit + tuple_num
print(tuple_fruit[1])
print(tuple_fruit[0:2])
print(tuple_fruit)
print(tuple_num)
print(tuple_add)
