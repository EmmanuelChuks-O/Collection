# dictionary

# syntax

# dict = {key1:value1, key2:value2, key3:value3....}

my_dict = {'name': 'Emmanuel', 'age': '29', 'city': 'Toronto'}
print(my_dict)

# dictionary operations

# access dictionary key values

print(my_dict['name'])

# add new key value pairs

my_dict['job'] = "Machine Learning Engineer"
print(my_dict)

# remove a key value pair

del my_dict['age']
print(my_dict)

# replace value of a key

my_dict['city'] = 'Etobicoke'
print(my_dict)